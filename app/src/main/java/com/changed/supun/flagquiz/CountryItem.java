package com.changed.supun.flagquiz;

public class CountryItem {

    //variables to store name and image
        String name;
        int image;


        //constructor
        public CountryItem(String name, int image){

            this.name = name;
            this.image = image;
        }


        //getters
        public String getName() {
            return name;
        }

        public int getImage() {
            return image;
        }

    }


